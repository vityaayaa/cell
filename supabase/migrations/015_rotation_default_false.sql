-- Поворот товара в ячейке теперь ВЫКЛЮЧЕН по умолчанию.
-- Раньше rotation_allowed default true — на практике менеджер про поворот
-- забывал, а он молча раздувал вместимость. Теперь его надо включить осознанно
-- (переключатель в настройках ячейки виден только для поворачиваемого товара —
-- unit с разной шириной и высотой).
--
-- Только МЕНЯЕМ DEFAULT для будущих строк. Существующие ячейки НЕ трогаем:
-- если где-то поворот уже включён осознанно — пусть остаётся. Клиентские точки
-- создания ячеек тоже пишут false.
alter table cells alter column rotation_allowed set default false;
