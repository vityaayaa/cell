import {
  P,
  Steps,
  Step,
  Callout,
  BlockHeading,
  ChapterHeading,
} from './docsUi'
import { ProcessFlowSchema } from './docsSchemas'

/* Tutorial IDs are referenced by the scroll-spy and the drawer, so they
   live here as named constants. */
export const TUTORIAL_ADMIN_ID = 'start-admin'
export const TUTORIAL_EMPLOYEE_ID = 'start-employee'

/* ─────────────────────────────────────────────────────────
   Employee tutorial — the end-to-end path a newcomer follows.
   ───────────────────────────────────────────────────────── */

export function EmployeeTutorial({
  /** When shown under the admin tutorial, use a quieter heading. */
  asSubsection = false,
}: {
  asSubsection?: boolean
}) {
  return (
    <div className="flex flex-col gap-1">
      <BlockHeading
        id={TUTORIAL_EMPLOYEE_ID}
        title={asSubsection ? 'Как работают сотрудники' : 'С чего начать'}
        subtitle={
          asSubsection
            ? 'Тот же путь, что проходит каждый сотрудник: обход → заявка → сборка.'
            : 'Вам дали телефон и попросили сделать обход? Вот весь путь по шагам.'
        }
      />

      {/* Chapter A — the big picture */}
      <ChapterHeading>Что вообще происходит</ChapterHeading>
      <P>
        Ваша задача — пройти вдоль стеллажа, посмотреть, сколько какого товара
        осталось, и внести это в телефон. Дальше приложение само посчитает, чего не
        хватает, и составит заявку на склад. Потом по этой заявке вы соберёте товар.
        Три шага: обход → заявка → сборка.
      </P>
      <ProcessFlowSchema />

      {/* Chapter B — walkthrough */}
      <ChapterHeading>Шаг 1. Обход стеллажа</ChapterHeading>
      <Steps>
        <Step n={1}>
          На главном экране нажмите «Начать новый обход». Приложение поведёт вас по
          ячейкам по очереди.
        </Step>
        <Step n={2}>
          На экране — карточка текущей ячейки: её адрес (например A6), какой это товар
          и сколько уже внесено. Найдите эту ячейку на стеллаже глазами.
        </Step>
        <Step n={3}>
          Посмотрите, сколько товара реально лежит в ячейке, и внесите число. Штучный
          товар (доски, трубы) — впишите количество штук. Товар навалом (сыпучее,
          мелочь) — двигайте ползунок, деления это пачки.
        </Step>
        <Step n={4}>
          Нажмите «Записать и дальше» — приложение сохранит и перейдёт к следующей
          ячейке. Если ячейку не считали — «Пропустить».
        </Step>
        <Step n={5}>
          Сверху видно прогресс «Обход: N из M» — сколько ячеек уже прошли. Идите,
          пока не пройдёте все.
        </Step>
      </Steps>
      <Callout variant="tip" title="Ошиблись?">
        Вернитесь к ячейке стрелками ‹ › или через кнопку «вся карта» и внесите
        заново — сохранится последнее число.
      </Callout>
      <Callout variant="info" title="Обход ведёт один человек">
        Не нужно, чтобы двое считали один стеллаж одновременно.
      </Callout>

      {/* Chapter C — order */}
      <ChapterHeading>Шаг 2. Заявка на склад</ChapterHeading>
      <Steps>
        <Step n={1}>
          Когда обход пройден, нажмите «→ К заявке». Приложение уже посчитало, чего и
          сколько не хватает — вы увидите готовый черновик.
        </Step>
        <Step n={2}>
          Просмотрите список. Каждая строка — товар и сколько пачек привезти. Нажмите
          на строку, чтобы поправить количество или убрать.
        </Step>
        <Step n={3}>
          Если чего-то не хватает в списке — «Добавить товар» и добавьте вручную.
        </Step>
        <Step n={4}>
          Всё верно — нажмите «Финализировать заявку». Теперь заявка зафиксирована и
          превратилась в чеклист для сборки.
        </Step>
      </Steps>
      <Callout variant="warning" title="Жёлтые «пограничные позиции» ⚠">
        Это где не хватает меньше одной пачки. Они НЕ попадают в заявку сами: ради
        пары штук ехать обычно не стоит. Нужно — нажмите и добавьте вручную.
      </Callout>

      {/* Chapter D — checklist */}
      <ChapterHeading>Шаг 3. Сборка по чеклисту</ChapterHeading>
      <Steps>
        <Step n={1}>Откройте чеклист — это ваш список для сборки на складе.</Step>
        <Step n={2}>
          Идите по складу и отмечайте, что взяли. Если чего-то нет или взяли не всё —
          это тоже можно отметить.
        </Step>
        <Step n={3}>
          Нужен бумажный список — нажмите «Печать», получите форму с клетками для
          галочек.
        </Step>
      </Steps>
      <Callout variant="info" title="Готово!">
        Вы прошли весь путь: обход → заявка → сборка. В следующий раз будет проще.
      </Callout>
    </div>
  )
}

/* ─────────────────────────────────────────────────────────
   Admin tutorial — one-time setup before employees can work.
   ───────────────────────────────────────────────────────── */

export function AdminTutorial() {
  return (
    <div className="flex flex-col gap-1">
      <BlockHeading
        id={TUTORIAL_ADMIN_ID}
        title="Настройка с нуля (для администратора)"
        subtitle="Прежде чем сотрудники начнут обходы, приложение нужно один раз настроить."
      />
      <Steps>
        <Step n={1}>
          Задайте стеллаж: сколько столбцов и рядов ячеек — под ваш реальный стеллаж.
          Ячейки можно делить на части и объединять обратно.
        </Step>
        <Step n={2}>
          Заполните каталог: добавьте товары (название, тип, размеры, размер пачки),
          объедините их в группы (Брусок, Труба…) и материалы (с цветом).
        </Step>
        <Step n={3}>Назначьте товары в ячейки — какая ячейка под какой товар.</Step>
        <Step n={4}>
          Задайте вместимость ячеек. Приложение считает её само по размерам, но если
          расчёт неверен — впишите вручную. Без вместимости ячейка не попадёт в
          заявку.
        </Step>
        <Step n={5}>
          Добавьте сотрудников в разделе «Пользователи». Теперь они могут делать
          обходы.
        </Step>
      </Steps>
      <Callout variant="tip" title="Дальше — работа сотрудников">
        Сотрудники работают по циклу обход → заявка → сборка. Как это устроено —
        смотрите туториал сотрудника ниже.
      </Callout>
    </div>
  )
}
